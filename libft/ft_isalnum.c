#include "libft.h"

bool	ft_isalnum(int c)
{
	if (c >= 48 && c <= 57)
		return (true);
	else if (c >= 65 && c <= 90)
		return (true);
	else if (c >= 97 && c <= 122)
		return (true);
	else
		return (false);
}
