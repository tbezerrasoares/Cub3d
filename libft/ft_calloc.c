#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*buff;

	buff = malloc(nmemb * size);
	if (buff != NULL)
		ft_memset(buff, 0, nmemb * size);
	return (buff);
}
