#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*blist;

	i = 0;
	blist = lst;
	while (blist != NULL)
	{
		blist = blist->next;
		i++;
	}
	return (i);
}
